package P2;

//Static Methods
public class Example {
	public static void main(String[] args) {
		int my_variable = 0;
		System.out.println("Printing variable: " + my_variable);
	}
}

/*
 * In the code above, main() is a static method. it needs to be static because
 * it called even when no object is created in the code. It does not need an
 * object to be present for its functioning. Hence, this method has the static
 * keyword attached to it.
 */